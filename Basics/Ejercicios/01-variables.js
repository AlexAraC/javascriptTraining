//var + nombre de variable + asignación de valor

var helloWorld = 'Hello, JavaScript!'
console.log(helloWorld)

helloWorld = 'Hello, JavaScript! Again'
console.log(helloWorld)

//let es de scope local, var es de scope global

let helloWorldLet = 'Hello, JavaScript with let!'
console.log(helloWorldLet)

helloWorldLet = 'Hello, JavaScript with let! Again'
console.log(helloWorldLet)

//const

const helloWorldConst = 'Hello, JavaScript with const!'
console.log(helloWorldConst)